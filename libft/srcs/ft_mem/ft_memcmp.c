#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const	char *str1;
	const	char *str2;

	str1 = (const char *)s1;
	str2 = (const char *)s2;
	while (n-- > 0)
	{
		if (*str1 != *str2)
			return ((unsigned char)*str1 - (unsigned char)*str2);
		str1++;
		str2++;
	}
	return (0);
}
