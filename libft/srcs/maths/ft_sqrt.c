int	ft_sqrt(unsigned int n)
{
	unsigned int i;

	if (n == 0 || n == 1)
		return (n);
	i = 2;
	while (i <= (n / i) && i < 65536)
	{
		if ((unsigned int)(i * i) == n)
			return (i);
		i++;
	}
	return (0);
}
