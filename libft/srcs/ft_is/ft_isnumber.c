#include "libft.h"

int		ft_isnumber(char *s)
{
	if (s && (*s == '-' || *s == '+'))
		s++;
	if (!ft_isdigit(*s))
		return (0);
	while (s && *s)
		if (!ft_isdigit(*s++))
			return (0);
	return (1);
}
